inline void 
GL_DrawModel(gl_model *Model,
             gl_shader_program *Shader,
             f32 *ModelMatrix,
             f32 *Colour)
{
    glUniformMatrix4fv(Shader->ModelLocation,
                       1,
                       GL_FALSE,
                       ModelMatrix);
    glUniform3fv(Shader->ColourLocation,
                 1,
                 Colour);
    glBindVertexArray(Model->VaoID);
    glDrawArrays(GL_TRIANGLES, 0, Model->VertCount);
}